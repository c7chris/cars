package com.chris.cars.service.objects;

/**
 * @author chris
 *
 */
public class DatamuseWord {

	private String word;
	
	private int score;
	
	private int numSyllables;

	/**
	 * @return the word
	 */
	public String getWord() {
		return word;
	}

	/**
	 * @param word the word to set
	 */
	public void setWord(String word) {
		this.word = word;
	}

	/**
	 * @return the score
	 */
	public int getScore() {
		return score;
	}

	/**
	 * @param score the score to set
	 */
	public void setScore(int score) {
		this.score = score;
	}

	/**
	 * @return the numSyllables
	 */
	public int getNumSyllables() {
		return numSyllables;
	}

	/**
	 * @param numSyllables the numSyllables to set
	 */
	public void setNumSyllables(int numSyllables) {
		this.numSyllables = numSyllables;
	}

}
